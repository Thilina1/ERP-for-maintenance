               mDate.setText("");
               sDate.setText("");
               wDetail.setText("");
               eDate.setText("");
               Cost.setText("");
               mName.setText("");
               status.getSelectedItem();
               note.setText("");
               
               
               showMessageDialog(null, "Successfully added equipment.");
