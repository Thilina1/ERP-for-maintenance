                snote=note.getText();
                
                //Note=note.getText();
                query = "INSERT INTO equipment_data (Equipment_Name, Equipment_Manufacture, Last_Maintanance_Date, Warrenty_detail, Start_date, End_date,Cost ,Main_Name ,status, sNote) "
                        + "VALUES ('"+eN+"','"+eM+"','"+lMD+"','"+wD+"','"+sD+"','"+eD+"','"+cost+"', '"+mN+"','"+sta+"','"+snote+"')";
               
               st.executeUpdate(query);
